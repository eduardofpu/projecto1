    private void jButtonDescontoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDescontoActionPerformed
        // TODO add your handling code here:
         float valorAtual,porcentagem,desconto,atualizado;
        
        
        
         valorAtual= Float.parseFloat(jTextFieldValorAtual.getText());
         porcentagem = Float.parseFloat(jTextFieldPorcentagem.getText());
        
        atualizado = (valorAtual) * (100 - porcentagem)/100 ;
        desconto = valorAtual - atualizado ;
        
        valorPorcentagem.setText(formato.format(desconto));
        valorAtualizado.setText(formato.format(atualizado));
        
        jLabel3.setText(valorDesconto.getMensagem());
       
    }//GEN-LAST:event_jButtonDescontoActionPerformed

