    private void jButtonAumentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAumentoActionPerformed
        // TODO add your handling code here:
        
        float valorAtual,porcentagem,aumento,atualizado;
        
        
        
        valorAtual= Float.parseFloat(jTextFieldValorAtual.getText());
         porcentagem = Float.parseFloat(jTextFieldPorcentagem.getText());
        
        aumento = valorAtual * (porcentagem)/100;
        atualizado = valorAtual + aumento ;
        
        valorPorcentagem.setText(formato.format(aumento));
        valorAtualizado.setText(formato.format(atualizado));
        
        jTextFieldValorAtual.requestFocus();
        
        jLabel3.setText(valorAumento.getMensagem());
    }//GEN-LAST:event_jButtonAumentoActionPerformed

